import json

import pandas as pd


def build_stock_selection_prompt(result_df: pd.DataFrame) -> str:
    candidates = result_df.to_dict(orient="records")

    return f"""
你是一个 A 股研究助手，负责基于量化筛选结果做结构化总结。

你的任务：
根据我提供的候选股票数据，从中整理出最终值得重点观察的股票。

当前模型逻辑：
1. 行业优先逻辑会先看行业平均涨跌幅、行业上涨占比和行业中位成交额，尽量弱化大体量行业的天然优势。
2. 选出优先行业后，再在行业内选择成交活跃、流动性较好的公司。
3. 基本面评分来自 ROE、ROE稳定性、净利润同比、营收同比、净利率、毛利率稳定性、现金流、资产负债率、EPS 等指标。
4. 行业内竞争力来自营收、净利润、ROE、毛利率、净利率、净利润增速、营收增速等行业分位。
5. 技术面评分来自均线、RSI、MACD、量能、回撤等指标，主要用于择时确认。
6. 技术/壁垒代理评分来自毛利率行业分位、ROE行业分位、ROE稳定性和毛利率稳定性。
7. 综合评分由基本面评分、行业内竞争力评分和技术面评分共同影响。
8. 股票名称、市场板块、行业板块已经包含在候选数据中。

重要限制：
1. 只能基于我提供的数据分析。
2. 不要编造新闻、财报、公告、内幕消息或未来涨跌。
3. 不要承诺收益。
4. 这不是投资建议，只是技术面和基本面研究总结。
5. 每只股票都要说明入选理由和风险点。
6. 优先选择综合评分高、行业地位较强、技术面结构较好、基本面质量较稳、风险提示较少的股票。

候选股票数据如下：
{json.dumps(candidates, ensure_ascii=False, indent=2)}

请按下面格式输出：

一、总体结论
说明本次候选股票整体质量、主要优势和主要风险。

二、最终股票列表
用表格输出：
排名 | 股票代码 | 股票名称 | 市场板块 | 行业板块 | 综合评分 | 技术评分 | 基本面评分 | 行业内竞争力评分 | 技术壁垒代理评分 | 细分龙头标签 | 入选理由 | 主要风险

三、观察重点
说明后续需要重点观察哪些行业景气、基本面质量和技术面择时信号。
"""
